theme_default <- function(x, ...) {
  fn <- theme_placement_factory(
    horizontal = get_option("tinytable_theme_default_horizontal", "c"),
    latex_float = get_option(
      "tinytable_theme_placement_latex_float",
      default = NULL
    )
  )
  x <- style_tt(x, finalize = fn)

  # bootstrap class
  bc <- if (length(x@bootstrap_class) == 0) {
    "table table-borderless"
  } else {
    x@bootstrap_class
  }
  x <- theme_tt(x, "bootstrap", class = bc)

  if (isTRUE(x@output %in% c("html", "bootstrap", "typst"))) {
    col <- if (x@output == "typst") "black" else "#d3d8dc"

    # top border
    if (x@output %in% c("html", "bootstrap") && length(x@names) == 0) {
      # For HTML with no column names, apply border to the first data row
      x <- style_tt(
        x,
        i = 1,
        line = "t",
        line_color = col,
        line_width = 0.1
      )
    } else {
      # For other cases, use the standard header position
      x <- style_tt(
        x,
        i = -x@nhead + 1,
        line = "t",
        line_color = col,
        line_width = 0.1
      )
    }
    # mid
    if (length(x@names) > 0) {
      x <- theme_tt(x, "bootstrap", class = bc)
      x <- style_tt(
        x,
        i = 0,
        line = "b",
        line_color = col,
        line_width = 0.05
      )
    }
    # bottom
    x <- style_tt(
      x,
      i = nrow(x),
      line = "b",
      line_color = col,
      line_width = 0.1
    )
  }

  return(x)
}
