#' Spanning labels to identify groups of rows or columns
#'
#' @export
#' @inheritParams tt
#' @inheritParams style_tt
#' @param i A vector of labels with length equal to the number of rows in `x`, or a named list of row indices to group. The names of the list will be used as labels. The indices represent the position where labels should be inserted in the original table. For example,
#' + `i=list("Hello"=5)`: insert the "Hello" label after the 4th row in the original table.
#' + `i=list("Hello"=2, "World"=2)`: insert the two labels consecutively after the 1st row in the original table.
#' + `i=list("Foo Bar"=0)`: insert the label in the first row after the header.
#' @param j String or named list
#' - Named list of column indices to group, ex: `j=list("A"=1:2,"B"=3:6)`. The names of the list will be used as labels. See below for more examples. Note: empty labels must be a space: " ".
#' - A single string when column names include the group name as a prefix, ex: group1_column1, group1_column2, etc.
#' @param ... Other arguments are ignored.
#' @return An object of class `tt` representing the table.
#' @param indent integer number of `pt` to use when indenting the non-labelled rows.
#' @template limitations_word_markdown
#' @details
#' Warning: The `style_tt()` can normally be used to style the group headers, as expected, but that feature is not available for Markdown and Word tables.
#' @examples
#'
#' # vector of row labels
#' dat <- data.frame(
#'   label = c("a", "a", "a", "b", "b", "c", "a", "a"),
#'   x1 = rnorm(8),
#'   x2 = rnorm(8)
#' )
#' tt(dat[, 2:3]) |> group_tt(i = dat$label)
#'
#' # named lists of labels
#' tt(mtcars[1:10, 1:5]) |>
#'   group_tt(
#'     i = list(
#'       "Hello" = 3,
#'       "World" = 8
#'     ),
#'     j = list(
#'       "Foo" = 2:3,
#'       "Bar" = 4:5
#'     )
#'   )
#'
#' dat <- mtcars[1:9, 1:8]
#' tt(dat) |>
#'   group_tt(i = list(
#'     "I like (fake) hamburgers" = 3,
#'     "She prefers halloumi" = 4,
#'     "They love tofu" = 7
#'   ))
#'
#' tt(dat) |>
#'   group_tt(
#'     j = list(
#'       "Hamburgers" = 1:3,
#'       "Halloumi" = 4:5,
#'       "Tofu" = 7
#'     )
#'   )
#'
#' x <- mtcars[1:5, 1:6]
#' tt(x) |>
#'   group_tt(j = list("Hello" = 1:2, "World" = 3:4, "Hello" = 5:6)) |>
#'   group_tt(j = list("Foo" = 1:3, "Bar" = 4:6))
#'
#' # column names with delimiters
#' dat <- data.frame(
#'   A_id = 1,
#'   A_a1 = 2,
#'   A_a2 = "3",
#'   B_b1 = 4,
#'   B_b2 = 5,
#'   B_C = 6
#' )
#' tt(dat) |> group_tt(j = "_")
#'
group_tt <- function(
    x,
    i = getOption("tinytable_group_i", default = NULL),
    j = getOption("tinytable_group_j", default = NULL),
    k = getOption("tinytable_group_k", default = NULL),
    indent = getOption("tinytable_group_indent", default = 1),
    ...) {
  # ... is important for ihead passing

  if (!inherits(x, "tinytable")) {
    stop("`x` must be generated by `tinytable::tt()`.", call. = FALSE)
  }
  if (is.null(i) && is.null(j) && is.null(k)) {
    stop("At least one of `i`, `j`, or `k` must be specified.", call. = FALSE)
  }
  assert_integerish(indent, lower = 0)

  if (isTRUE(check_string(j))) {
    if (any(grepl(j, x@names, fixed = TRUE))) {
      j_delim <- j_delim_to_named_list(x = x, j = j)
      x@names <- j_delim$colnames
      j <- j_delim$groupnames
    } else {
      j <- NULL
    }
  }

  if (is.null(i) && is.null(j) && is.null(k)) {
    return(x)
  }

  # vector of labels
  if (isTRUE(check_atomic_vector(i))) {
    i <- sanitize_group_vec2list(i)
  }

  i <- sanitize_group_index(i, hi = nrow(x) + 1, orientation = "row")
  j <- sanitize_group_index(j, hi = ncol(x), orientation = "column")

  # increment indices eagerly
  i <- unlist(i)

  if (!is.null(i)) {
    if (x@group_n_i > 0) {
      stop(
        "Only one row-wise `group_tt(i = ...)` call is allowed.",
        call. = FALSE
      )
    }

    x@group_n_i <- length(i)
    x@nrow <- x@nrow + x@group_n_i
    x@group_index_i <- as.numeric(i) + cumsum(rep(1, length(as.numeric(i)))) - 1

    if (isTRUE(indent > 0)) {
      idx_indent <- setdiff(seq_len(nrow(x)), i + seq_along(i) - 1)
      x <- style_tt(x, i = idx_indent, j = 1, indent = indent)
    }
  }

  if (!is.null(j)) {
    x@nhead <- x@nhead + 1
  }

  # handle k parameter immediately (not lazily) since it modifies the underlying data
  if (!is.null(k)) {
    x <- group_eval_k(x, k)
  }

  # apply group labels lazily (but not k, which was handled above)
  cal <- call("group_eval", i = i, j = j, k = NULL, indent = indent)
  x@lazy_group <- c(x@lazy_group, list(cal))

  return(x)
}

sanitize_group_vec2list <- function(vec) {
  if (is.factor(vec)) {
    vec <- as.character(vec)
  }
  rle_result <- rle(vec)
  idx <- cumsum(c(1, utils::head(rle_result$lengths, -1)))
  idx <- as.list(idx)
  names(idx) <- rle_result$values
  return(idx)
}

sanitize_group_index <- function(idx, hi, orientation) {
  if (is.null(idx)) {
    return(idx)
  }
  assert_list(idx, named = TRUE)
  for (n in names(idx)) {
    if (orientation == "row") {
      assert_integerish(idx[[n]], len = 1, lower = 1, upper = hi, name = n)
    } else {
      assert_integerish(idx[[n]], lower = 1, upper = hi, name = n)
    }
  }
  # allow duplicated indices for consecutive rows
  # if (anyDuplicated(unlist(idx)) > 0) stop("Duplicate group indices.", call. = FALSE)
  out <- lapply(idx, function(x) min(x):max(x))
  return(out)
}


j_delim_to_named_list <- function(x, j) {
  nm <- x@names

  # Find which elements contain the delimiter, and optionally j. Others are left as is
  check_for_multiple_delims <- any(
    lengths(gregexec(pattern = j, text = nm)) > 1L
  )
  if (check_for_multiple_delims) {
    warning(
      "Multiple delimiters found in column names. Only the first delimiter will be used for grouping."
    )
  }

  indices <- grepl(j, nm, fixed = TRUE)
  groupnames <- sub(
    pattern = paste0(j, ".*"),
    replacement = "",
    x = nm[indices]
  )
  indices <- which(grepl(j, nm, fixed = TRUE))
  groupnames <- split(indices, groupnames)

  # Extract suffixes (after delimiter) used as new sub-headers
  colnames <- sub(pattern = paste0(".*?", j), replacement = "", x = nm)

  out <- list(colnames = colnames, groupnames = groupnames)
  return(out)
}


# Internal function to handle k parameter matrix insertion
group_eval_k <- function(x, k) {
  if (is.null(k)) {
    return(x)
  }

  # k should be a list with position and matrix
  if (!is.list(k) || length(k) != 2) {
    stop("k must be a list with exactly 2 elements: position and matrix", call. = FALSE)
  }

  positions <- k[[1]]
  matrix_data <- k[[2]]

  # Validate position parameter
  assert_integerish(positions, lower = 1)
  assert_matrix(matrix_data)

  # Validate matrix_data (can be matrix, data.frame, or vector)
  if (is.matrix(matrix_data)) {
    assert_matrix(matrix_data, min_rows = 1, min_cols = 1)
  } else if (is.data.frame(matrix_data)) {
    assert_data_frame(matrix_data, min_rows = 1, min_cols = 1)
  } else if (is.vector(matrix_data)) {
    # Convert vector to matrix
    matrix_data <- matrix(matrix_data, nrow = 1)
  } else {
    stop("k[[2]] must be a matrix, data.frame, or vector", call. = FALSE)
  }

  # Always convert to data frame
  matrix_df <- as.data.frame(matrix_data, stringsAsFactors = FALSE)

  # Check column count assertion
  if (ncol(matrix_df) != ncol(x@table_dataframe)) {
    stop("Matrix must have the same number of columns as the table", call. = FALSE)
  }

  # Set column names to match the table
  names(matrix_df) <- names(x@table_dataframe)

  # Simplify logic by standardizing positions and matrix rows
  matrix_rows <- nrow(matrix_df)

  # If single position but multiple matrix rows, replicate the position
  if (length(positions) == 1 && matrix_rows > 1) {
    positions <- rep(positions[1], matrix_rows)
  }

  # If multiple positions but single matrix row, replicate the matrix row
  if (length(positions) > 1 && matrix_rows == 1) {
    matrix_df <- matrix_df[rep(1, length(positions)), , drop = FALSE]
    matrix_rows <- nrow(matrix_df)
  }

  # Validate that positions and matrix rows match
  if (length(positions) != matrix_rows) {
    stop("Length of positions must equal number of matrix rows after standardization", call. = FALSE)
  }

  # Calculate the actual row indices where k matrix rows will be inserted
  k_indices <- c()
  for (pos in positions) {
    # Position logic: position N means after row N, so inserted row is at N+1
    # But we need to account for previously inserted rows
    inserted_row_index <- pos + 1
    # Count how many k rows were inserted before this position
    prev_insertions <- sum(positions < pos)
    actual_index <- inserted_row_index + prev_insertions
    k_indices <- c(k_indices, actual_index)
  }

  # Process each matrix row at its corresponding position
  # Sort by position in descending order to avoid index shifting issues
  pos_order <- order(positions, decreasing = TRUE)

  for (i in seq_along(pos_order)) {
    idx <- pos_order[i]
    pos <- positions[idx]
    matrix_row <- matrix_df[idx, , drop = FALSE]

    # Insert the matrix row at the specified position
    # Position logic: 1 means after first row, 2 means after second row, etc.
    if (pos > nrow(x@table_dataframe)) {
      # Insert at the end
      x@table_dataframe <- rbind(x@table_dataframe, matrix_row)
    } else {
      # Insert after the specified row position
      before_rows <- x@table_dataframe[1:pos, , drop = FALSE]
      after_rows <- x@table_dataframe[(pos + 1):nrow(x@table_dataframe), , drop = FALSE]
      x@table_dataframe <- rbind(before_rows, matrix_row, after_rows)
    }
  }

  # Update all slots that need to be modified
  x@data <- x@table_dataframe
  x@table_input <- x@data
  x@nrow <- nrow(x@table_dataframe)

  # Update group tracking for k rows
  x@group_n_i <- x@group_n_i + length(k_indices)
  if (is.null(x@group_index_i) || length(x@group_index_i) == 0) {
    x@group_index_i <- k_indices
  } else {
    x@group_index_i <- c(x@group_index_i, k_indices)
  }

  return(x)
}
